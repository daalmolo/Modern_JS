// 1. arithmetic operators

let numX;

numX = 20 + 20; // addition

numX = 20 - 10; // subtraction

numX = 10 * 10; // product

numX = 50 / 10; // quotient

numX = 5 % 2;   // module

numX = 5 ** 2;  // exponent 


// increment operators
numX = 1;

numX++;         // increment
numX--;         // decrement


// Assignment opperators

numX = 100;

numX += 10;
numX -= 20;
numX *= 0.5;
numX /= 10;
numX %= 3;
numX **= 3;
